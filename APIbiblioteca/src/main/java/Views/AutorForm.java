    private void guardarAutorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarAutorActionPerformed
        String nombre = textNombre.getText().trim();
        String apellido = textApellido.getText().trim();
        String nacionalidad = textNacionalidad.getText().trim();

        if (nombre.isEmpty() || apellido.isEmpty() || nacionalidad.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor complete todos los campos");
            return;
        }

        try {
            Autor autor = new Autor();
            autor.nombre = nombre;
            autor.apellido = apellido;
            autor.nacionalidad = nacionalidad;

            boolean exito = ApiService.insertarAutor(autor);
            if (exito) {
                JOptionPane.showMessageDialog(this, "Autor insertado correctamente");
                this.dispose(); // cerrar el formulario
            } else {
                JOptionPane.showMessageDialog(this, "Error al insertar autor");
            }
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Error de conexi√≥n con el servidor");
        }
    }//GEN-LAST:event_guardarAutorActionPerformed

